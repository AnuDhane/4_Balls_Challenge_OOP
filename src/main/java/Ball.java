public class Ball {
    public int diameter=0;
    public int xLocation=0;
    public int yLocation=0;
    public int  speed=0;

    public Ball(int diameter, int xLocation, int yLocation, int speed) {
        this.diameter = diameter;
        this.xLocation = xLocation;
        this.yLocation = yLocation;
        this.speed = speed;
    }

}
